    private void btnAnularNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnularNotaActionPerformed
        // TODO add your handling code here:
        
        try{
            
            String message = "";
            String motivoAnulado = this.txtMotivo.getText().trim();
            
            if(motivoAnulado.equals("")){
                JOptionPane.showMessageDialog(null, "Ingrese el motivo de la anulaci√≥n", Module.titleMessage, JOptionPane.CANCEL_OPTION);
                return;
            }
            
            ETipoMovimiento objTipoMov = new ETipoMovimiento();
            objTipoMov.setId(tipoNota);
            
            EMovimientos objMov = new EMovimientos();
            objMov.setId(Module.id);
            objMov.setTipoMovimiento(objTipoMov);
            objMov.setFechaAnulado(LocalDateTime.now(ZoneId.of( "America/Lima" )));
            objMov.setMotivoAnulado(motivoAnulado);
            objMov.setEstado("ANULADO");
            
            message = clsMovimiento.anularMovimiento(objMov);
            if(message.equals("Anulado correctamente")){
                this.txtSerie.setText("");
                this.txtNumero.setText("");
                this.txtMotivo.setText("");
                this.formActive.getCaller().loadTable();
                JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, message, Module.titleMessage, JOptionPane.CANCEL_OPTION);
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString(), Module.titleMessage, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAnularNotaActionPerformed

