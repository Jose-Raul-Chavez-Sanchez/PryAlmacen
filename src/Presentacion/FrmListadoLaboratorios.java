    @Override
    public final void loadTable(){
        try{
            
            for (int i=this.defaultTable.getRowCount()-1;i>=0;i--){
                this.defaultTable.removeRow(i);
            }
            
            Laboratorios clsLaboratorios = new Laboratorios();
            ArrayList arrayList = clsLaboratorios.getAll();
            
            for (int i = 0; i < arrayList.size(); i++) {
                ELaboratorios lab = (ELaboratorios)arrayList.get(i);
                this.defaultTable.addRow(new Object[]{});
                this.defaultTable.setValueAt(lab.getId(), i, 0);
                this.defaultTable.setValueAt(lab.getDescripcion(), i, 1);
                this.defaultTable.setValueAt(lab.getEstado(), i, 2);
            }
            
            EColorTableCell foreColor = new EColorTableCell();
            foreColor.setColumnIndex(2);
            tblLaboratorios.getColumnModel().getColumn(2).setCellRenderer(foreColor);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString(), Module.titleMessage, JOptionPane.ERROR_MESSAGE);
        }
    }
