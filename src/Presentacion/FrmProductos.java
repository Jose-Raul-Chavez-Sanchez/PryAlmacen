    public void setSelectedValue(JComboBox comboBox, int value){
        EComboBox item;
        for (int i = 0; i < comboBox.getItemCount(); i++){
            item = (EComboBox)comboBox.getItemAt(i);
            if (item.getId() == value){
                comboBox.setSelectedIndex(i);
                break;
            }
        }
    }
