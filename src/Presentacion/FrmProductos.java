    private void cleanForm(){
        this.txtProducto.setText("");
        this.txtStockMinimo.setText("");
        this.txtStockMaximo.setText("");
        this.txtStockActual.setText("");
        this.txtPrecioCompra.setText("");
        this.txtUtilidad.setText("");
        this.txtPrecioVta.setText("");
        formActive.getCaller().loadTable();
        llenarComboLaboratorio();
        llenarComboCategorias();
        llenarComboMarcas();
        llenarComboUndMedida();
    }
    
    private void txtStockMinimoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStockMinimoKeyTyped
        // TODO add your handling code here:
        char tecla = evt.getKeyChar();
        String codigo = txtStockMinimo.getText();
        int punto = codigo.indexOf(".")+1;
        
        if(punto == 0){
            if(!Character.isDigit(tecla) && tecla != java.awt.event.KeyEvent.VK_BACK_SPACE && tecla != KeyEvent.VK_PERIOD){
                evt.consume();
                getToolkit().beep();
            }
        }else{
            if(!Character.isDigit(tecla) && tecla != java.awt.event.KeyEvent.VK_BACK_SPACE){
                evt.consume();
                getToolkit().beep();
            }
        }
    }//GEN-LAST:event_txtStockMinimoKeyTyped

